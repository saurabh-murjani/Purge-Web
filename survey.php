		<span class="events"><h3 id='yo'>Polythene Usage Survey of shops in Bhopal</h3></span>
		<h2 class="content"> Purge Manit conducted a survey on shops of Bhopal after taking permission from Nagar Nigam, Bhopal. The survey was done to collect data on polythense that the shopkeepers were using and testing if they were in accordance with PLASTIC WASTE MANAGEMENT ACT 2011.<p> According to the ACT:<br><br>
			1) Thickness of polythene should not be less than 40 micron.<br>
			2) Polythene should be virgin(not recycled).<br>
			3) Shopkeeper should charge minimum price for providing polythene to his customers.<br>
			4) Firm name,registration and address should be provided on polythene by manufacturer.<p> A report from the data collected is being made and would be submitted to Nagar Nigam so that necessary actions can be taken.
