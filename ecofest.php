		<h2 class="content">On the occassion of Gandhi Jayanti, Purge organised a mega fest in MANIT, Bhopal in rememberance of Mahatma Gandhi. With this fest, Purge took a major step towards it's aim of promoting cleanliness in the college campus. Various dignitories like Mr. Babulal Gaud (Hon. Home Minister, M.P.) , Mr. Tarun Kumar Pithode (Senior Officer, Vyapam) and Dr. M.M. Malik (Deputy Director, MANIT) attended the fest and encouraged the whole team of Purge. The whole fest was divided into various events like estabilishing Gandhi Udhyan, developing a Vertical Garden etc.<p> Gandhi Udhyan- A garden near the academic section of MANIT was turned into a clean garden to promote greenery. The grass is cutted and the plantation is now being done in such a way that the plantation forms the initials of Mahatma Gandhi. This was a symbol of rememberance for Bapu.<p>Vertical Garden- Plastic bottles were collected from various offices of the college and they were used as pot for plants in formin a vertical garden on a wall. The wall became a sign of attraction and it promoted the reuse of plastic waste.<p>Treasure Hunt- A treasure hunt was organized among students of MANIT based on the theme of reducing plastic waste in order to make students of the college excited towards the cause which Purge promotes.<p> Also, a ceremony was organized to throw light on the principles of Mahatma Gandhi. Cleanest Hostel of Manit and Cleanest Department of Manit were awarded for their act of maintaining cleanliness.</h2>
